export const colorCode = (color) => {
  color.toLowerCase();
  let num = 0;
  COLORS.map((item, index) => {
    if (color === item) return (num = index);
  });
  return num;
};

export const COLORS = [
  "black",
  "brown",
  "red",
  "orange",
  "yellow",
  "green",
  "blue",
  "violet",
  "grey",
  "white",
];
